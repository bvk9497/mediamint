const mongoose = require("mongoose");

const userSchema = mongoose.Schema(
  {
    ID: {
      type: Number,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    gender: {
      type: String,
      required: true,
    },
    status: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const User = mongoose.model("users",userSchema);
module.exports = User;
